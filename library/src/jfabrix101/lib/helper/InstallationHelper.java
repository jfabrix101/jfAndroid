package jfabrix101.lib.helper;

import java.io.File;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.RandomAccessFile;
import java.util.UUID;

import android.content.Context;

/**
 * Questa classe si occupa di generare e recuperare un ID
 * univoco di installazione.
 * 
 * l'informazione (file INSTALLATION) viene memorizzata all'interno
 * della cartella privata dell'aplicazione. In questo modo se viene
 * disinstallata e successivamente installata l'ID univo cambia
 * 
 */
public class InstallationHelper {
	private static final String INSTALLATION = "INSTALLATION";
	private static String sID = null;

	/**
	 * Recupera un ID univoco per l'installazione.
	 * @param context
	 * @return
	 */
	public synchronized static String getInstallationId(Context context) {
		if (sID == null) {
			File installation = new File(context.getFilesDir(), INSTALLATION);
			try {
				if (!installation.exists()) {
					writeInstallationFile(installation);
				}
				sID = readInstallationFile(installation);
			} catch (Exception e) {
				throw new RuntimeException(e);
			}
		}
		return sID;
	}

	private static String readInstallationFile(File installation) throws IOException {
		RandomAccessFile f = new RandomAccessFile(installation, "r");
		byte[] bytes = new byte[(int) f.length()];
		f.readFully(bytes);
		f.close();
		return new String(bytes);
	}

	private static void writeInstallationFile(File installation) throws IOException {
		FileOutputStream out = new FileOutputStream(installation);
		String id = UUID.randomUUID().toString();
		out.write(id.getBytes());
		out.close();
	}
}
